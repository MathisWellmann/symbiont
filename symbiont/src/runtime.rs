/// Singleton runtime instance.
static RUNTIME: OnceLock<Runtime> = OnceLock::new();

/// Manages the lifecycle of the temporary dylib crate: creation, compilation,
/// loading, and hot-reloading.
///
/// Function dispatch is lock-free: each evolvable function reads its cached
/// pointer via a single `AtomicPtr::load`. Library lifetime is managed by
/// keeping the current and previous libraries alive, which bounds memory
/// to two loaded `.so` files at any time while guaranteeing that in-flight
/// calls through the previous version's pointers remain valid.
pub struct Runtime {
    /// Path to the temporary dylib crate directory.
    crate_dir: PathBuf,
    /// Path to the compiled `.so` / `.dylib` / `.dll` file.
    so_path: PathBuf,
    /// Monotonically increasing version counter for versioned `.so` paths
    /// to defeat `dlopen` caching.
    version: AtomicU64,
    /// Function signatures for validation of LLM-generated code.
    fn_sigs: Vec<String>,
    /// The currently loaded library and its predecessor.
    /// Keeping two versions alive ensures that in-flight calls through
    /// the previous version's function pointers don't segfault.
    /// The Mutex is only taken during reload, never on the call hot-path.
    libraries: Mutex<LibrarySlot>,
    /// Declarations (kept for fn_ptr updates on reload).
    decls: &'static [EvolvableDecl],
}

/// Holds the current and previous loaded libraries.
struct LibrarySlot {
    current: Option<Library>,
    previous: Option<Library>,
}

/// Look up all declared symbols in `lib` and store their addresses
/// in the corresponding `AtomicPtr` fields of each declaration.
///
/// # Safety
///
/// The caller must ensure `lib` is a valid loaded library containing
/// symbols with signatures matching the declarations.
unsafe fn update_fn_ptrs(lib: &Library, decls: &[EvolvableDecl]) -> Result<()> {
    for decl in decls {
        let c_name =
            CString::new(decl.name).expect("function name must not contain interior NUL bytes");
        let sym: libloading::Symbol<*const ()> = unsafe {
            lib.get(c_name.as_bytes_with_nul())
                .map_err(|e| Error::DylibLoad(format!("symbol '{}' not found: {e}", decl.name)))?
        };
        decl.fn_ptr.store(*sym as *mut (), Ordering::Release);
    }
    Ok(())
}

impl Runtime {
    /// Initialize the symbiont runtime.
    ///
    /// Creates a temporary dylib crate from the declarations generated by `evolvable!`,
    /// compiles it, and loads the resulting shared library.
    ///
    /// # Panics
    ///
    /// Panics if called more than once.
    pub async fn init(decls: &'static [EvolvableDecl]) -> Result<&'static Runtime> {
        if decls.is_empty() {
            return Err(Error::NoEvolvableFunctions);
        }

        let fn_sigs = Vec::from_iter(decls.iter().map(|d| d.signature.to_string()));

        // Create a stable temp directory based on function names
        let mut hasher = DefaultHasher::new();
        for d in decls {
            d.name.hash(&mut hasher);
        }
        let hash = hasher.finish();
        let crate_dir = std::env::temp_dir().join(format!("symbiont-evolvable-{hash:x}"));
        std::fs::create_dir_all(crate_dir.join("src"))?;

        // Write Cargo.toml
        let cargo_toml = generate_cargo_toml();
        std::fs::write(crate_dir.join("Cargo.toml"), cargo_toml)?;

        // Write src/lib.rs from all default_source entries
        let lib_rs = generate_lib_rs(decls);
        std::fs::write(crate_dir.join("src").join("lib.rs"), &lib_rs)?;
        info!("Created temp dylib crate at {}", crate_dir.display());

        // Compile
        compile_dylib(&crate_dir).await?;

        // Find and load the .so
        let so_path = find_so(&crate_dir)?;
        let lib = unsafe {
            Library::new(&so_path).map_err(|e| {
                Error::DylibLoad(format!("Failed to load {}: {e}", so_path.display()))
            })?
        };

        // Cache function pointers (lock-free after this point)
        unsafe { update_fn_ptrs(&lib, decls)? };

        let runtime = Runtime {
            crate_dir,
            so_path,
            version: AtomicU64::new(1),
            fn_sigs,
            libraries: Mutex::new(LibrarySlot {
                current: Some(lib),
                previous: None,
            }),
            decls,
        };

        RUNTIME
            .set(runtime)
            .map_err(|_| Error::AlreadyInitialized)?;
        Ok(RUNTIME.get().expect("just set"))
    }

    /// Get the function signature strings for all evolvable functions.
    pub fn fn_sigs(&self) -> &[String] {
        &self.fn_sigs
    }

    /// Generate LLM response, then parse, validate, compile, and hot-swap.
    ///
    /// The `llm_response` should contain a Rust code block (optionally markdown-fenced)
    /// with implementations matching the declared evolvable function signatures.
    async fn evolve<CompletionModelT>(
        &self,
        agent: &Agent<CompletionModelT>,
        prompt: &str,
    ) -> Result<()>
    where
        CompletionModelT: CompletionModel + 'static,
    {
        let t0 = Instant::now();

        let llm_response = agent.prompt(prompt).await?;
        info!("llm_response: {llm_response}");

        // Parse Rust from markdown fences
        let mut ast = parse_rust_code(&llm_response).map_err(|_| Error::CouldNotParseRust)?;

        // Validate signatures match declarations
        validate_generated_ast(&mut ast, &self.fn_sigs)?;

        // Write validated AST to temp crate's lib.rs
        let lib_rs_path = self.crate_dir.join("src").join("lib.rs");
        let formatted = prettyplease::unparse(&ast);
        std::fs::write(&lib_rs_path, &formatted)
            .map_err(|e| Error::WriteLib(format!("Failed to write lib.rs: {e}")))?;
        info!("Wrote evolved lib.rs to {}", lib_rs_path.display());

        // Recompile
        compile_dylib(&self.crate_dir).await?;

        // Copy .so to versioned path to defeat dlopen caching
        let version = self.version.fetch_add(1, Ordering::SeqCst);
        let versioned_so = self.crate_dir.join(format!(
            "libsymbiont_evolvable_v{version}{}",
            dylib_extension()
        ));
        std::fs::copy(&self.so_path, &versioned_so)?;

        // Load new library
        let new_lib = unsafe {
            libloading::Library::new(&versioned_so).map_err(|e| {
                Error::DylibLoad(format!("Failed to load {}: {e}", versioned_so.display()))
            })?
        };

        // Update cached function pointers (atomic stores with Release ordering).
        // After this point, new calls go through the new library.
        unsafe { update_fn_ptrs(&new_lib, self.decls)? };

        // Rotate libraries: current becomes previous, new becomes current.
        // The previous library is dropped here. This is safe because:
        // - Compilation takes hundreds of ms to seconds
        // - Any in-flight call through the previous version's pointers
        //   completes in microseconds at most
        // - By the time we reach this reload, all calls from two versions
        //   ago have long since finished
        {
            let mut slot = self.libraries.lock().expect("libraries Mutex poisoned");
            slot.previous = slot.current.take();
            slot.current = Some(new_lib);
        }

        info!(
            "Hot-reloaded evolvable dylib (version {version}). LLM generation, validation, compilation, and swapping took {}ms",
            t0.elapsed().as_millis()
        );

        Ok(())
    }

    // TODO: might want to rename this to `evolve`, and rename original `evolve` to something else. Backpressure should always be included in public method.
    /// Process an LLM response: parse, validate, compile, and hot-swap.
    ///
    /// The `llm_response` should contain a Rust code block (optionally markdown-fenced)
    /// with implementations matching the declared evolvable function signatures.
    ///
    /// If the constrained generation fails due to the LLM output,
    /// steer it with some prompt change and try function evolution again until it succeeds.
    pub async fn evolve_with_backpressure<CompletionModelT>(
        &self,
        agent: &Agent<CompletionModelT>,
        base_prompt: &str,
    ) -> Result<()>
    where
        CompletionModelT: CompletionModel + 'static,
    {
        let mut prompt = base_prompt.to_string();

        while let Err(e) = self.evolve(agent, &prompt).await {
            info!("Function evolution error: {e}");

            prompt = base_prompt.to_string();

            use Error::*;
            match e {
                    NoRustCode => prompt.push_str(
                        "Your response did not contain a rust code block. Please try again and make sure its wrapped like this: ```CODE```",
                    ),
                    CouldNotParseRust => prompt.push_str(
                        "Your response did not contain valid Rust code. Please try again",
                    ),
                    WriteLib(_) => todo!(),
                    SignatureMismatch {
                        name: _,
                        expected,
                        got,
                    } => prompt.push_str(&format!(
                        "Generated function signature miss-match. Expected ```{expected}```, Got ```{got}```"
                    )),
                    CompilationFailed(ref stderr) => prompt.push_str(&format!(
                        "The generated code failed to compile. Compiler output:\n```\n{stderr}\n```\nPlease fix the compilation errors."
                    )),
                    e => {
                        warn!("Unhandled error: {e}");
                        return Err(e)
                    },
                }
        }

        Ok(())
    }

    /// Path to the temporary crate directory.
    pub fn crate_dir(&self) -> &Path {
        &self.crate_dir
    }
}

fn generate_cargo_toml() -> String {
    r#"[package]
name = "symbiont-evolvable"
version = "0.1.0"
edition = "2024"

[lib]
crate-type = ["dylib"]

[dependencies]
"#
    .to_string()
}

fn generate_lib_rs(decls: &[EvolvableDecl]) -> String {
    decls
        .iter()
        .map(|d| d.default_source)
        .collect::<Vec<_>>()
        .join("\n\n")
}

fn dylib_extension() -> &'static str {
    if cfg!(target_os = "macos") {
        ".dylib"
    } else if cfg!(target_os = "windows") {
        ".dll"
    } else {
        ".so"
    }
}

/// Find the compiled shared library in the temp crate's target directory.
fn find_so(crate_dir: &Path) -> Result<PathBuf> {
    let target_dir = crate_dir.join("target").join("debug");

    let prefix = if cfg!(target_os = "windows") {
        ""
    } else {
        "lib"
    };
    let name = format!("{prefix}symbiont_evolvable{ext}", ext = dylib_extension());
    let so_path = target_dir.join(&name);

    if so_path.exists() {
        Ok(so_path)
    } else {
        Err(Error::DylibLoad(format!(
            "Compiled dylib not found at {}",
            so_path.display()
        )))
    }
}
